
package mvc.Model;

/**
 * The type Player.
 */
public class Player {
    private boolean isWhite;


    /**
     * Instantiates a new Player.
     *
     * @param isWhite the is white
     */
    public Player(boolean isWhite) {
        this.isWhite = isWhite;
    }

    /**
     * Is white boolean.
     *
     * @return the boolean
     */
    public boolean isWhite() {
        return isWhite;
    }
}
